package homework_13_05_2025;

public class Operations {

    public int getSum(int a,int b) {
        int resultSum = a+b;
        return resultSum;
    }
    public int getDiff(int a,int b) {
        int resultDiff = a-b;
        return resultDiff;
    }
    public int getMulti(int a,int b) {
        int resultMulti = a*b;
        return resultMulti;
    }
    public double getDivision(int a, int b) {
        double resultDiv = a/b;
        return resultDiv;
    }
    public int getRemainder(int a, int b) {
        int resultRem = a%b;
        return resultRem;
    }
}
